                    <h2>Four Decades of Telling Whistler’s Stories</h2>
                    <img src="images/guidedby.jpg" alt="Four Decades of Telling Whistler’s Stories" style="width:100%; height:auto;"/>
                    <p>For a Whistler photographer, few things beat landing the coveted front cover of a ski magazine.</p>
                    <p>Greg Griffith knows. SKI, Skiing, Powder—he’s been on them all over a career spanning 30 plus years, selling the dream (and by extension the magazines) of life in the mountains.</p>
                    <p>Forty years ago this winter, Griffith also made the cover of the inaugural issue of this magazine with a pristine shot of four skiers swooshing down Gandy Dancer (now Ross’s Gold) on Blackcomb Mountain.</p>
                    <p>It’s a prescient image, a photo full of potential and promise, of untold adventures yet to come. The nascent Whistler Village sits in the valley below, a mere outpost compared to today. Only two years before this shot was taken, this had been the site of the town’s garbage dump! The Coast Mountains sit majestically in the background, stretching on endlessly, timelessly, across the horizon.</p>
                    <p>And yet, for those four twenty-something-year-old skiers in the foreground, you can tell it’s all about the moment: there’s just something about the freedom in their movement, a bluebird day, knee-deep fresh powder under foot, an entire mountain to themselves.</p>
                    <p>“We had no idea that the resort was going to grow as big as it did,” laughs Griffith, of that carefree, living-in-the-moment, time of life.</p>
                    <p>In the 40 years since, Whistler has transformed into a four-season international destination. Along the way, Whistler Magazine has been there, telling the stories and documenting the journey. At our 40th anniversary, we are taking time to reflect on how far we’ve come and where we might be going as Whistler faces unprecedented challenges in the months, and quite possibly, the years ahead.</p>
                    <h4>Beginnings – 40 years ago</h4>
                    <p>Little did anyone realize at the time the role magazines like Whistler Magazine would play in “selling the dream.”</p>
                    <p>It turns out there was a huge appetite for glossy spreads showcasing fun in the mountains. Readers wanted to feel for themselves what they read and saw in the pages within.</p>
                    <p>“Local journalism has always been incredibly important for Whistler because it is about telling the stories that really make up the essence of our community and who we are,” says Tourism Whistler President and CEO Barrett Fisher.</p>
                    <p>Griffith remembers in 1979 he was hired by Tourism BC to take photos of skiing, particularly as it related to the fanfare around the much-talked-about Blackcomb Mountain that was set to open the following year. He remembers they enlisted a snow cat for several laps to get the perfect shot. He found it on Gandy Dancer.</p>
                    <p>“Blackcomb had this view, looking straight down (to the Village),” says Griffith. “That was a very new shot as far as the look of the ski area.”</p>
                    <p>And that was the name of the game for Blackcomb Mountain: Trying to set itself apart if it was to have any chance at competing with the established Whistler Mountain, which had been up and running since 1966.</p>
                    <p>As Hugh Smythe, the man tasked with opening the new resort, recalls the eternal question of the time was: What can we do to differentiate ourselves?</p>
                    <p>“Blackcomb was under the shadow of Whistler Mountain,” he says. “We were the underdog and the new kid on the block.”</p>
                    <p>Just imagine 40 years ago: Blackcomb had five lifts and no alpine skiing on opening day in 1980. Whistler Mountain, by comparison, had 13 lifts and 12 new runs that year alone. As its half-page ad in that first issue of Whistler Magazine boldly states: “We have it all.”</p>
                    <p>Smythe decided to go head-to-head: They would charge the same to ski at Blackcomb as on Whistler—$300 for a season pass, $15 for a day pass.</p>
                    <p>As it turns out, geography worked in Blackcomb’s favour—steeper fall-line skiing and longer runs were enough to pique an interest. Still, they needed more.</p>
                    <p>Service would have to set them apart—friendly outgoing staff, perks like cleaning car windshields. And, they had to sell the dream to the magazines.</p>
                    <p>Smythe muses that he likely spoke to Paul Burrows, editor of Whistler Magazine in 1980 and founder and editor of Whistler’s first newspaper The Whistler Question, every week. There was a lot to talk about of course. In addition to Blackcomb, development of Whistler Village, under the Whistler Village Land Co., was simultaneously under way.</p>
                    <p>In the opening pages of that first magazine, the president of the Whistler Village Land Co. Ltd, Terry Minger, apologizes. He writes: “Please excuse our incompleteness and rough edges—we’ve all been working very hard but the likes of St. Moritz, Sun Valley or Chamonix wasn’t built in a day!”</p>
                    <h4>Coming of Age</h4>
                    <p>Indeed. Whistler Village almost wasn’t built at all. As Minger penned those words in 1980, the world was at the beginning of a severe economic recession that would last until early 1983. Interest rates hit a staggering 22 per cent and the Whistler Village Land Co was teetering on the edge of bankruptcy by late 1981.</p>
                    <p>With unfinished buildings and a new resort hanging in the balance, the provincial government was called in to help…to the tune of $21 million to get Whistler back on track.</p>
                    <p>Clearly there was a sense of the untapped potential lying in the mountains. Whistler Magazine reported in its first issue that: “Last year (1979), with 10 chair lifts, Whistler was one of the most popular ski mountains in Canada, attracting 400,000 skiers in the winter months.”</p>
                    <p>It was simply too good to let it go belly-up.</p>
                    <p>Whistler and Blackcomb, though separate entities at the time, together made up one of the largest ski areas in North America, boasting the two longest vertical drops of any ski area. Skiers were taking notice; it wasn’t long before more followed suit.</p>
                    <p>In 1992, Snow Country Magazine, one of North America’s most prominent ski magazines, voted Whistler Resort the Number One Ski Resort in North America, ushering in a new era of being on top.</p>
                    <p>Ralph Forsyth, who moved to Whistler 30 years ago to be a ski instructor, remembers the annual rankings well. It always seemed as though Whistler or Blackcomb was on top and every autumn, a buoyant mood permeated the town when the rankings were released.</p>
                    <p>“Everybody had a spring in their step. You could feel it,” he says.</p>
                    <p>It was the ’90s in Whistler; it was the wild west, there was a boundless energy in town and a feeling that Whistler knew it was No. 1. It certainly behaved as though it was No. 1. There was the World Ski and Snowboard Festival and Winterfest and the Fire and Ice show, so many things pushing the boundaries all the time.</p>
                    <p>“We were doing such cool stuff,” says Forsyth, now a municipal councillor.</p>
                    <p>When Blackcomb and Whistler merged in 1998 under the banner of Intrawest, the party just kept going; the No. 1 ranking remained.</p>
                    <h4>A Look Ahead</h4>
                    <p>It hasn’t always been easy. Throughout it all, Whistler has weathered the peaks and valleys of a resort existence—the lows of global recessions and tourism droughts, the highs of the 2010 Olympic Games and showing off to a global audience.</p>
                    <p>The challenges of today, however, aren’t like the challenges of the past. The COVID-19 pandemic has brought a new era of global uncertainty and worry for the future. As in the past however, Whistler looks to its natural assets to shape its future.</p>
                    <p>The resort saw stronger results in the summer than most destinations, Fisher says, reaching 60 per cent hotel occupancy in August, as people looked to spend more time outdoors.</p>
                    <p>Part of the success is the community—banding together, working as one, with one common goal to be the best it can be.</p>
                    <p>“We as a community have always come together in a spirit of collaboration,” says Fisher. “It might sound old fashioned but it truly is a reality."</p>
                    <p>“A lot of that is based on that entrepreneurial spirit that Whistler was founded on.”</p>
                    <p>With safety and COVID protocols top priority this season, Tourism Whistler is keeping its eye on the future. “I do believe there are always opportunities for growth coming out of challenges,” says Fisher.</p>
                    <p>In the years to come, it’s crucial to understand how to balance resort visitation in the future and sustain a year-round tourism economy, says Fisher. What does “responsible tourism” look like going forward? How do we educate our guests about our fragile mountain environment that we want to protect and enjoy for decades to come?</p>
                    <p>“Our purpose is to honour and celebrate the magnitude of Whistler’s rugged environment and the bold and adventurous spirit of the people who love it,” she adds.</p>
                    <p>For Whistler Magazine that, in turn, means many more stories about the people who live and visit here and the special place they call Whistler.</p>
